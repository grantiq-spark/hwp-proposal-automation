// HWP Proposal Cloud — pure client-side
// 1) Read HWPX (zip) and extract <hp:t> text
// 2) Call OpenAI Chat Completions API with user-supplied key
// 3) Bundle local-runner.zip { generated.md, pipeline/*.ps1, run.cmd, run.ps1, README.txt }
// 4) Offer downloads

const PIPELINE_FILES = [
  "paste_word_to_new_hangul.ps1",
  "stretch_hwpx_layout.ps1",
  "reflow_with_hangul_engine.ps1",
  "compare_rendered_pages.ps1",
  "create_docx_from_text.ps1",
  "extract_hwpx_text.ps1"
];

const RUN_CMD = `@echo off
setlocal
cd /d "%~dp0"
powershell -NoProfile -ExecutionPolicy Bypass -File ".\\run.ps1" %*
pause
`;

const RUN_PS1 = `param(
  [string]$BriefMarkdown = ".\\generated.md",
  [string]$OutputFinal   = ".\\final.hwpx",
  [string]$Factor        = "2.02"
)

$ErrorActionPreference = "Stop"
$Root = $PSScriptRoot
Set-Location $Root

$pipeline = Join-Path $Root "pipeline"
$source   = Join-Path $Root "source.docx"
$rich     = Join-Path $Root "word_rich_paste.hwpx"
$stretch  = Join-Path $Root "stretched.hwpx"

Write-Host "=== Step 1: Markdown -> source.docx (Word COM)"
powershell -NoProfile -ExecutionPolicy Bypass -STA -File (Join-Path $pipeline "create_docx_from_text.ps1") \`
  -InputText $BriefMarkdown -OutputDocx $source

Write-Host "=== Step 2: Word DOCX -> Hangul HWPX"
powershell -NoProfile -ExecutionPolicy Bypass -STA -File (Join-Path $pipeline "paste_word_to_new_hangul.ps1") \`
  -SourceDocx $source -OutputHwpx $rich

Write-Host "=== Step 3: stretch layout"
powershell -NoProfile -ExecutionPolicy Bypass -File (Join-Path $pipeline "stretch_hwpx_layout.ps1") \`
  -InputHwpx $rich -OutputHwpx $stretch -Factor $Factor

Write-Host "=== Step 4: reflow through Hangul"
powershell -NoProfile -ExecutionPolicy Bypass -STA -File (Join-Path $pipeline "reflow_with_hangul_engine.ps1") \`
  -InputHwpx $stretch -OutputHwpx $OutputFinal

Write-Host "=== DONE: $OutputFinal"
`;

