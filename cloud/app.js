// DOCX -> HWPX Runner Builder (no AI, no API key)
// Bundles user's DOCX + pipeline/*.ps1 + GUI launcher (convert.vbs) + fallback run.cmd

const PIPELINE_FILES = [
  "paste_word_to_new_hangul.ps1",
  "stretch_hwpx_layout.ps1",
  "reflow_with_hangul_engine.ps1",
  "compare_rendered_pages.ps1"
];

// Primary GUI launcher — double-click to convert, no terminal visible
const CONVERT_VBS = `' DOCX -> HWPX GUI launcher
' Double-click this file. PowerShell runs hidden in the background.
Option Explicit

Dim shell, fso, scriptDir, docxPath, runPs1, finalPath, ans, exitCode
Set shell = CreateObject("WScript.Shell")
Set fso = CreateObject("Scripting.FileSystemObject")
scriptDir = fso.GetParentFolderName(WScript.ScriptFullName)
docxPath = scriptDir & "\\source.docx"
runPs1   = scriptDir & "\\run.ps1"
finalPath = scriptDir & "\\final.hwpx"

If Not fso.FileExists(docxPath) Then
  MsgBox "source.docx 가 이 폴더에 없습니다." & vbCrLf & vbCrLf & _
         "이 ZIP을 풀어 그대로 둔 채로 실행해 주세요.", _
         vbCritical, "DOCX -> HWPX"
  WScript.Quit 1
End If

If Not fso.FileExists(runPs1) Then
  MsgBox "run.ps1 가 없습니다. ZIP을 모든 파일과 함께 풀었는지 확인하세요.", _
         vbCritical, "DOCX -> HWPX"
  WScript.Quit 1
End If

ans = MsgBox( _
  "이 폴더의 source.docx 를 final.hwpx 로 변환합니다." & vbCrLf & vbCrLf & _
  "약 30초~1분간 Word와 한컴 한글이 자동으로 잠깐씩 떴다 사라집니다." & vbCrLf & _
  "그 동안 마우스/키보드를 건드리지 말아 주세요." & vbCrLf & vbCrLf & _
  "지금 시작할까요?", _
  vbQuestion + vbYesNo + vbDefaultButton1, _
  "DOCX -> HWPX 변환")
If ans <> vbYes Then WScript.Quit 0

' Delete stale final.hwpx if any
If fso.FileExists(finalPath) Then fso.DeleteFile finalPath, True

' Run hidden, wait for completion
exitCode = shell.Run( _
  "powershell.exe -NoProfile -ExecutionPolicy Bypass -File """ & runPs1 & """", _
  0, True)

If exitCode = 0 And fso.FileExists(finalPath) Then
  ans = MsgBox( _
    "완료! final.hwpx 가 생성되었습니다." & vbCrLf & vbCrLf & _
    "지금 폴더를 열까요?", _
    vbInformation + vbYesNo, _
    "DOCX -> HWPX 변환")
  If ans = vbYes Then
    shell.Run "explorer.exe """ & scriptDir & """", 1, False
  End If
ElseIf fso.FileExists(finalPath) Then
  MsgBox "변환은 됐지만 일부 단계에서 경고가 있었습니다 (exit " & exitCode & ")." & vbCrLf & _
         "final.hwpx 는 정상 생성되었으니 한글로 열어보세요.", _
         vbExclamation, "DOCX -> HWPX 변환"
Else
  MsgBox _
    "변환에 실패했습니다 (exit code " & exitCode & ")." & vbCrLf & vbCrLf & _
    "체크리스트:" & vbCrLf & _
    " 1) Microsoft Word 설치 및 정상 실행 가능?" & vbCrLf & _
    " 2) 한컴 한글 2020 한 번 직접 실행해 라이선스 통과시켰는지?" & vbCrLf & _
    " 3) 한글 도구 > 환경설정 > 보안 > 스크립트 보안 = 보통/낮음?" & vbCrLf & vbCrLf & _
    "상세 로그를 보려면 같은 폴더의 run.cmd 를 더블클릭해 직접 실행해 보세요.", _
    vbCritical, "DOCX -> HWPX 변환"
End If
`;

// Fallback console runner (for troubleshooting)
const RUN_CMD = `@echo off
setlocal
cd /d "%~dp0"
echo === DOCX -> HWPX (console mode)
echo.
powershell -NoProfile -ExecutionPolicy Bypass -File ".\\run.ps1"
echo.
echo === Script finished. Press any key to close.
pause >nul
`;

